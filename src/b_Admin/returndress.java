    public void displayRentedDresses() {
    config conf = new config();
    try {
        // We use CASE to display 'Rented' instead of 'Paid' visually
        String query = "SELECT r.r_id AS 'Rental ID', r.r_cust_name AS 'Customer', "
                     + "d.d_name AS 'Dress Name', r.r_date AS 'Rent Date', "
                     + "CASE WHEN r.r_status = 'Paid' THEN 'Rented' ELSE r.r_status END AS 'Status' "
                     + "FROM tbl_rentals r "
                     + "JOIN tbl_dresses d ON r.d_id = d.d_id "
                     + "WHERE r.r_status = 'Paid'";
        
        rented_tbl.setModel(DbUtils.resultSetToTableModel(conf.getData(query)));
        config.styleTable(rented_tbl);
    } catch (Exception e) {
        System.out.println("Display Error: " + e.getMessage());
    }
}

