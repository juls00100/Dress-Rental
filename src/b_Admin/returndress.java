    private void manage_returnsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_manage_returnsMouseClicked
       int row = rented_tbl.getSelectedRow();
    if (row == -1) {
        JOptionPane.showMessageDialog(null, "Please select a rented item!");
        return;
    }

    String rid = rented_tbl.getValueAt(row, 0).toString();
    config conf = new config();

    String did = "";
    try {
        ResultSet rs = conf.getData("SELECT d_id FROM tbl_rentals WHERE r_id = '" + rid + "'");
        if (rs.next()) {
            did = rs.getString("d_id");
        }
        rs.getStatement().getConnection().close();
    } catch (Exception e) { }

    if (!did.isEmpty()) {
        // SUCCESS ACTION:
        // 1. Mark the rental record as 'Returned' (History)
        conf.updateRecord("UPDATE tbl_rentals SET r_status = 'Returned' WHERE r_id = '" + rid + "'");
        
        // 2. Mark the dress as 'Available' (Inventory)
        conf.updateRecord("UPDATE tbl_dresses SET d_status = 'Available' WHERE d_id = '" + did + "'");
        
        JOptionPane.showMessageDialog(null, "Success! Status updated to Available.");
        displayRentedDresses(); 
    }
    }//GEN-LAST:event_manage_returnsMouseClicked

