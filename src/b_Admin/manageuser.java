    private void search_barKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_search_barKeyReleased

        String text = search_bar.getText();

        // We search the confirmed columns from your database
        String sql = "SELECT user_id, full_name, email, address, role, status FROM Users WHERE "
        + "user_id LIKE '%" + text + "%' OR "
        + "full_name LIKE '%" + text + "%' OR "
        + "email LIKE '%" + text + "%' OR "
        + "address LIKE '%" + text + "%' OR "
        + "number LIKE '%" + text + "%' OR "
        + "role LIKE '%" + text + "%' OR "
        + "status LIKE '%" + text + "%'";

        try {
            config conf = new config();
            // Uses the getData method you have in your config.java
            java.sql.ResultSet rs = conf.getData(sql);

            if (rs != null) {
                // Update the table with matching results only
                user_tbl.setModel(net.proteanit.sql.DbUtils.resultSetToTableModel(rs));
            }
        } catch (Exception e) {
            System.out.println("Search Error: " + e.getMessage());
        }
    }//GEN-LAST:event_search_barKeyReleased

